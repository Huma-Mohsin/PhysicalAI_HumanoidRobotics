// Function to translate an entire document or section while preserving structure
export const translateDocument = async (content: string, targetLang: 'ur' | 'en'): Promise<string> => {
  // This function handles more complex content with mixed text and code
  if (targetLang === 'en') {
    // If switching back to English, return original content
    return content;
  }

  // For Urdu translation, we'll identify and preserve different content types
  let result = content;

  // Extract code blocks to preserve them
  const codeBlocks: string[] = [];
  result = result.replace(CODE_BLOCK_PATTERN, (match) => {
    codeBlocks.push(match);
    return `__CODE_BLOCK_${codeBlocks.length - 1}__`;
  });

  // Extract HTML tags to preserve them
  const htmlTags: string[] = [];
  result = result.replace(HTML_TAG_PATTERN, (match) => {
    htmlTags.push(match);
    return `__HTML_TAG_${htmlTags.length - 1}__`;
  });

  // Translate the remaining text content
  result = await translateText(result, targetLang);

  // Restore HTML tags
  result = result.replace(/__HTML_TAG_(\d+)__/g, (match, index) => {
    const tagIndex = parseInt(index, 10);
    return htmlTags[tagIndex] || match;
  });

  // Restore code blocks
  result = result.replace(/__CODE_BLOCK_(\d+)__/g, (match, index) => {
    const blockIndex = parseInt(index, 10);
    return codeBlocks[blockIndex] || match;
  });

  return result;
};

