// Function to check if content is already in Urdu
export const isContentInUrdu = (text: string): boolean => {
  // Simple check: if text contains Urdu characters (Unicode range 0600-06FF, 0750-077F, FB50-FDFF, FE70-FEFF)
  const urduRegex = /[\u0600-\u06FF\u0750-\u077F\uFB50-\uFDFF\uFE70-\uFEFF]/;
  return urduRegex.test(text);
};

